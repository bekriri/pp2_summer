# data types in python

# int
x = 10
print(type(x))

# float
y = 3.14
print(type(y))

# string
name = "Bek"
print(type(name))

# boolean
is_student = True
print(type(is_student))

# list
fruits = ["apple", "banana", "cherry"]
print(type(fruits))

# tuple
coords = (10, 20)
print(type(coords))

# dictionary
person = {"name": "Bekri", "age": 21}
print(type(person))

# set
nums = {1, 2, 3, 4}
print(type(nums))
